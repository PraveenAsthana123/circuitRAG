import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';

// Vite dev-server proxies API requests to the local gateway so CORS stays simple.
export default defineConfig({
  plugins: [react()],
  server: {
    port: 3000,
    proxy: {
      '/api': {
        target: process.env.VITE_API_BASE_URL || 'http://localhost:8080',
        changeOrigin: true,
      },
    },
  },
  build: {
    outDir: 'dist',
    sourcemap: true,
  },
});
